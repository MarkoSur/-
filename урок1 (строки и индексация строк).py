example = 'дифракция'
print (example [0])     #отобразить первый символ
print (example [-1])    #отобразить последний символ
print (example [4:])    #отобразить вторую половину (с нечетным количеством символов)
print (example [::-1])  #отобразить строку наоборот
print (example [1::2])  #отобразить каждый второй символ строки
